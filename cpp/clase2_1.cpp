// desarrollar un programa que dado 2 enteros inicializados  me muestre la suma, la resta, la multiplicacion y la division

#include <iostream>

using namespace std; 


int main(){

    int num1 = 20, num2 = 5;

    cout <<num1 << " + " <<num2 << " = "<<num1+num2 <<endl;
    cout <<num1 << " - " <<num2 << " = "<<num1-num2 <<endl;
    cout <<num1 << " * " <<num2 << " = "<<num1*num2 <<endl;
    cout <<num1 << " / " <<num2 << " = "<<num1/num2 <<endl;

    return 0;
}



 